    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        System.out.println(txtExDate.getDate());
        if (txtName.getText().equals("") || txtDescription.getText().equals("") || txtValue.getText().equals("") || txtExDate.getDate().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "Add offer details correctly.");
        } else {
            try {
                String date = new DateConverter().convert(txtExDate.getDate().toString());
                int id = DBConnection.iudReturnId("INSERT INTO offer (offer_id, name, value, description, exp_date) values ('" + txtOfferId.getText() + "','" + txtName.getText() + "','" + txtValue.getText() + "','" + txtDescription.getText() + "','" + date + "')");
                JOptionPane.showMessageDialog(this, "Offer Added Successfully.");

                sendOfferEmail(id, txtValue.getText());

                loadsup();
                clearAll();

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

