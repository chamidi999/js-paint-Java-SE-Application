    private void sendOfferEmail(int id, String value) {
        try {
            ResultSet rs = DBConnection.search("SELECT c.id, c.email, SUM(i.price) AS total_price FROM customer c JOIN invoice i ON i.customer_id = c.id WHERE i.status = '1' GROUP BY i.customer_id");
            while (rs.next()) {
                if (rs.getDouble("total_price") >= 20000) {
                    DBConnection.iudReturnId("INSERT INTO customer_has_offer (customer_id, offer_id, status) values ('" + rs.getString("id") + "','" + id + "','1')");
                    sendOffer(rs.getString("email"), value);
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

