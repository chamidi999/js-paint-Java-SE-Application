    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       
        try {
            String email = txtEmail.getText();
            String password = txtPassword.getText();
            ResultSet rs = DBConnection.search("SELECT * FROM admin WHERE email='" + email + "'");

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "Your Email does not match !!");
            } else {
                if (rs.getString("password").equals(password)) {
                    JOptionPane.showMessageDialog(this, "Successfully Login");
                    new Invoice().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Your Password does not match !!");
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

