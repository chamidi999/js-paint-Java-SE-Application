private String generateCustomerNumber() {
        String id = "";
        try {

            ResultSet rs = DBConnection.searchWithBackword("select * from customer");
            if (!rs.next()) {
                id = "CUS1000";
            } else {
                rs.last();
                //int i = rs.
                String dbid = rs.getString("customer_id");
                String gg = dbid.split("S")[1];
                int i = (Integer.parseInt(gg)) + 1;
                id = "CUS" + i;
            }

            System.out.println(id);

        } catch (Exception e) {
            e.printStackTrace();
        }
        return id;
    }

