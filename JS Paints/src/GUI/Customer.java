    private void txtCustomerSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCustomerSearchKeyReleased
         // TODO add your handling code here:
         try {
            if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
                jListDS.grabFocus();
                jListDS.setSelectedIndex(0);
            } else if (txtCustomerSearch.getText().equals("")) {
                jScrollPaneDS.setVisible(false);
            } else {
                ResultSet rs = DBConnection.search("select * from customer where customer_id like '%" + txtCustomerSearch.getText() + "%' AND status = '1'");
                Vector v = new Vector();
                while (rs.next()) {
                    v.add(rs.getString("customer_id"));
                }
                jListDS.setListData(v);
                if (v.equals("")) {
                    jScrollPaneDS.setVisible(false);
                }
                jScrollPaneDS.setVisible(true);
                jPanel8.updateUI();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtCustomerSearchKeyReleased

